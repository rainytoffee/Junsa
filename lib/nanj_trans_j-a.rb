  def self.nanj_translate(sentence,a_or_j)
      translated = []

    case a_or_j
    when :a
      sentence.chars do |ch|
      if JtoA_DICTIONARY.has_key?(ch)
        JtoA_DICTIONARY.each do |j,a|
          if ch == j
            translated << a
          end
        end
      else
        translated << ch
        end
     end

   when :j
     replaced_sentence = sentence.gsub("ンゴ","置換用置換")
     replaced_sentence.chars do |ch|
     if AtoJ_DICTIONARY.has_key?(ch)
       AtoJ_DICTIONARY.each do |a,j|
         if ch == a
           translated << j
         end
       end
     else
       translated << ch
     end #do
     end #if

    end #when
       pre_translated = translated.join.encode("utf-8")
      return "#{sentence} -> #{pre_translated.gsub("置換用置換","ンゴ")}"
  end #def

